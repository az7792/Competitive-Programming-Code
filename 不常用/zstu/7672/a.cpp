void solve()
{
    int n;
    set<int> s;
    cin >> n;
    if (n == 0)
        s.insert(0);
    while (n != 0)
    {
        s.insert(n % 10);
        n /= 10;
    }
    cout << min(3, (int)s.size()) << endl;
}
int main()
{
    ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
    srand(time(nullptr));
    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
