ll gcd(ll a, ll b)
{
    while (b != 0)
    {
        ll temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}
void solve()
{
    ll x, y, z;
    cin >> x >> y >> z;
    if (z == 0)
        cout << (x == 0 || y == 0 ? "YES" : "NO") << endl;
    else
        cout << (z % gcd(x, y) == 0 ? "YES" : "NO") << endl;
}
